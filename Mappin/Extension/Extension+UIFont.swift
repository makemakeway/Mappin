import UIKit

extension UIFont {
    var mainFontBlack: UIFont {
        return UIFont(name: "CookieRunOTF-Black", size: 17)!
    }
    var mainFontBold: UIFont {
        return UIFont(name: "CookieRunOTF-Bold", size: 17)!
    }
    var mainFontRegular: UIFont {
        return UIFont(name: "CookieRunOTF-Regular", size: 17)!
    }

    var smallFontBlack: UIFont {
        return UIFont(name: "CookieRunOTF-Black", size: 14)!
    }

    var smallFontBold: UIFont {
        return UIFont(name: "CookieRunOTF-Bold", size: 14)!
    }

    var smallFontRegular: UIFont {
        return UIFont(name: "CookieRunOTF-Regular", size: 14)!
    }

    var titleFontBlack: UIFont {
        return UIFont(name: "CookieRunOTF-Black", size: 20)!
    }

    var titleFontBold: UIFont {
        return UIFont(name: "CookieRunOTF-Bold", size: 20)!
    }

    var titleFontRegular: UIFont {
        return UIFont(name: "CookieRunOTF-Regular", size: 20)!
    }
}
